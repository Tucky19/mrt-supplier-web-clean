function normalizeRefs(
  refs?: Array<string | { brand?: string; partNo?: string }>
): string[] {
  return (refs ?? [])
    .map((ref) => {
      if (typeof ref === "string") return ref;
      return ref?.partNo ?? "";
    })
    .filter(Boolean);
}

function normalizeSpec(
  spec?: string,
  specifications?: Array<string | { label?: string; value?: string | number; unit?: string }>
): string {
  if (spec?.trim()) return spec.trim();

  return (specifications ?? [])
    .map((item) => {
      if (typeof item === "string") return item;
      const label = item.label ?? "";
      const value = item.value ?? "";
      const unit = item.unit ?? "";
      return `${label} ${value}${unit}`.trim();
    })
    .filter(Boolean)
    .join(", ");
}

function normalizeSpecifications(
  specifications?: Array<string | { label?: string; value?: string | number; unit?: string }>
) {
  const normalized = (specifications ?? [])
    .map((item) => {
      if (typeof item === "string") {
        return {
          label: "Specification",
          value: item,
        };
      }

      const label = String(item.label ?? "").trim();
      const value = item.value;
      const unit = String(item.unit ?? "").trim();

      if (!label || value === undefined || value === null || value === "") {
        return null;
      }

      return {
        label,
        value: unit ? `${value} ${unit}` : value,
      };
    })
    .filter((item): item is NonNullable<typeof item> => Boolean(item));

  return normalized.length > 0 ? normalized : undefined;
}

