function normalizeRefs(
  refs?: Array<string | { brand?: string; partNo?: string }>
): string[] {
  return (refs ?? [])
    .map((ref) => {
      if (typeof ref === "string") return ref;
      return ref?.partNo ?? "";
    })
    .filter(Boolean);
}

function normalizeSpec(
  spec?: string,
  specifications?: Array<string | { label?: string; value?: string | number; unit?: string }>
): string {
  if (spec?.trim()) return spec.trim();

  return (specifications ?? [])
    .map((item) => {
      if (typeof item === "string") return item;
      const label = item.label ?? "";
      const value = item.value ?? "";
      const unit = item.unit ?? "";
      return `${label} ${value}${unit}`.trim();
    })
    .filter(Boolean)
    .join(", ");
}

function normalizeSpecifications(
  specifications?: Array<string | { label?: string; value?: string | number; unit?: string }>
) {
  const normalized = (specifications ?? [])
    .map((item) => {
      if (typeof item === "string") {
        return {
          label: "Specification",
          value: item,
        };
      }

      const label = String(item.label ?? "").trim();
      const value = item.value;
      const unit = String(item.unit ?? "").trim();

      if (!label || value === undefined || value === null || value === "") {
        return null;
      }

      return {
        label,
        value: unit ? `${value} ${unit}` : value,
      };
    })
    .filter((item): item is NonNullable<typeof item> => Boolean(item));

  return normalized.length > 0 ? normalized : undefined;
}

function normalize(item: RawDonaldson): Product {
  const partNo = String(item.partNo || "").toUpperCase().trim();

  const brand = "donaldson"; // 🔒 fix brand ให้ตรงเสมอ

  const categoryMap: Record<string, string> = {
    air_filter: "air_filter",
    filter: "filter",
    fuel_filter: "filter",
  };

  const category =
    categoryMap[item.category?.toLowerCase?.()] || "filter";

  const spec =
    normalizeSpec(item.spec, item.specifications) ||
    "Standard Industrial Filter";

  const officialUrl =
    item.officialUrl === null
      ? undefined
      : item.officialUrl ||
        `https://shop.donaldson.com/store/en-us/product/${partNo}`;

  const imageUrl = item.imageUrl || item.image;
  const refs = normalizeRefs(item.refs);
  const crossReferences = normalizeRefs(item.cross_reference ?? item.crossRefs);
  const specifications = normalizeSpecifications(item.specifications);

  return {
    id: `donaldson-${partNo.toLowerCase()}`, // 🔒 format เดียวทั้งระบบ
    partNo,
    brand,
    category,
    title: item.title || `Donaldson Filter ${partNo}`,
    spec,
    imageUrl,
    specifications,
    refs: refs.length > 0 ? refs : undefined,
    crossReferences: crossReferences.length > 0 ? crossReferences : undefined,
    stockStatus: item.stockStatus || "request",
    officialUrl,
  };
}

function normalizePartNo(value: string): string {
  return String(value ?? "")
    .trim()
    .toUpperCase()
    .replace(/[^A-Z0-9]/g, "");
}

